class DepartmentController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        return view('backend.department.all', ['departments' => Department::all()]);
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        return response()->view('backend.department.add', ['faculties' => Faculty::all()]);
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'faculty' => ['required', Rule::exists('faculties', 'id')],
            'department-name' => ['required']
        ]);

        Department::create(['faculty_id' => $request->get('faculty'), 'name' => $request->get('department-name')]);

        return redirect(route('department.all'));
    }

